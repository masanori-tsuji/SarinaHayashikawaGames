//*****************************************************************************
// 静的メンバ変数初期化
//*****************************************************************************
LPDIRECTINPUT8 CInput::m_pDinput = NULL;

//=============================================================================
// コンストラクタ
//=============================================================================
CInput::CInput()
{
	m_pDevice = NULL;
}
//=============================================================================
// デストラクタ
//=============================================================================
CInput::~CInput()
{
}
//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CInput::Init(HINSTANCE hInstance, HWND hWnd)
{
	if (FAILED(DirectInput8Create(hInstance, DIRECTINPUT_VERSION, IID_IDirectInput8, (void**)&m_pDinput, NULL)))
	{
		return E_FAIL;
	}
	return S_OK;
}
//=============================================================================
// 終了処理
//=============================================================================
void CInput::Uninit(void)
{
	if (m_pDevice != NULL)
	{
		m_pDevice->Unacquire();
		m_pDevice->Release();
		m_pDevice = NULL;
	}
	if (m_pDinput != NULL)
	{
		m_pDinput->Release();
		m_pDinput = NULL;
	}
}